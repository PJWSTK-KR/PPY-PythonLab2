number1 = float(input("liczba 1>"))
number2 = float(input("liczba 2>"))
operation = input("operacja>")
if operation == "+":
    print(number1 + number2)
elif operation == "-":
    print(number1 - number2)
elif operation == "*":
    print(number1 * number2)
elif operation == "/":
    print(number1 / number2)
elif operation == "^":
    print(number1 ** number2)
elif operation == "%":
    print(number1 % number2)